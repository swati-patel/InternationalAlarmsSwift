import UIKit

class AppFonts {
    
    static func largeMediumFont() -> UIFont {
        return UIFont.systemFont(ofSize: 20.0, weight: .medium)
    }
    
    static func mediumFont() -> UIFont {
        return UIFont.systemFont(ofSize: 15.0, weight: .medium)
    }
    
    static func mediumFontLarger() -> UIFont {
        return UIFont.systemFont(ofSize: 18.0, weight: .medium)
    }
    
    static func extraLargeFont() -> UIFont {
        return UIFont.systemFont(ofSize: 24.0, weight: .light)
    }
    
    static func smallRegularFont() -> UIFont {
        return UIFont.systemFont(ofSize: 14.0, weight: .regular)
    }
    
    static func sectionTitleFont() -> UIFont {
        return UIFont.systemFont(ofSize: 12.0, weight: .medium)
    }
    
    static func headerTitleFont() -> UIFont {
        return UIFont.systemFont(ofSize: 16.0, weight: .medium)
    }
    
    static func infoButtonFont() -> UIFont {
        return UIFont.systemFont(ofSize: 12.0, weight: .regular)
    }
}